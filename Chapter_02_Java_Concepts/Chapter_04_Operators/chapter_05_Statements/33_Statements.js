// JavaScript Statements Overview

// 1. Conditional Statements
if (10 > 5) {
    console.log("Statement executed");
}

// 2. Block Statements
{
    let a = 1;
    let b = 2;
    console.log(a + b);
}

// 3. Selection Statements (Switch)
let color = "red";
switch(color) {
    case "red": console.log("Color is red"); break;
    default: console.log("Unknown color");
}
